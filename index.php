			<h1> Book Coverage</h1>
			<p>This probability and statistics textbook covers:</p>
			<ul>
				<li>Basic concepts such as random experiments, probability axioms,
					conditional probability, and counting methods</li>
				<li>Single and multiple random variables (discrete, continuous, and
					mixed), as well as moment-generating functions, characteristic 
					functions, random vectors, and inequalities</li>
				<li>Limit theorems and convergence</li>
				<li>Introduction to mathematical statistics, in particular, Bayesian and classical statistics</li>
				<li>Random processes including processing of random signals, Poisson
					processes, discrete-time and continuous-time Markov chains, and 
					Brownian motion</li>
				<li>Simulation using MATLAB, R, and Python</li>
			</ul>
			
