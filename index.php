			<h1>Welcome</h1>
			<p>This site is the homepage of the textbook Introduction to Probability, 
				Statistics, and Random Processes by Hossein Pishro-Nik. It is an open access 
				peer-reviewed textbook intended for undergraduate as well as first-year 
				graduate level courses on the subject. This probability textbook can be used by both students and 
				practitioners in engineering, mathematics, finance, and other related fields. </p>
			<p>The site includes:</p>
			<ul>
			    <li>The entire textbook</li>
				<li>Short video lectures that aid in learning the material</li>
				<li>Online probability calculators for important functions and distributions</li>
				<li>A solutions manual for instructors</li>
				<li><a href='https://www.dropbox.com/sh/iuhdrbq1yqf8t8q/AAAV6npl5xFkqfvTd2jSEjjwa?dl=0' target='_blank'>Lecture slides</a></li>
